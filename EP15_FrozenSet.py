#  FrozenSet
# เราไม่สามารถเปลี่ยนเปลงสมาชิก แก้ไข ลบ หรือเพิ่ม ไม่ได้เลย
# เหมาะสำหรับข้อมูล สำคัญคงที่ 

fruit=set(['มะม่วง','มะยม','มะนาว']) # สร้างตัวแปร Set ธรรมดา
fruit.add('ทุเรียน') # เพิ่ม
fruit.discard('มะยม') # ลบ 
print(fruit) # set ธรรมดาสามารถจัดการข้อมูลได้ 

# วิธีการสร้าง frozenset สำหรับข้อมูลที่สำคัญ

fruit2=frozenset(['แตงโม','ชมพู่','สตอเบอรี่']) 
print(fruit2) # ตัวแปรนี้ไม่สามารถแก้ไข ได้เลย 
