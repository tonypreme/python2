number10[-1]="แก่ไข" 
print("หลังแก้ไข :" ,number10)

# ---------------------------------

# แสดงผลด้วย loop
number11=[1,2,3,4,5,6,7,8,9]
# ถ้ารู้รอบที่ชัดเจนใช้ for 

sum=0
for x in number11:
    sum+=x # sum=sum+x
print(sum)

# ---------------------------------

# ตรวจสอบข้อมูล 
# เช็คข้อมูลว่า ในกลุ่มข้อมูลนี้มีข้อมูล นี้หรือเปล่า

fruit=["มะม่วง","มะนาว",",มะยม"]
if "มะม่วง" in fruit:
    print("มี")
