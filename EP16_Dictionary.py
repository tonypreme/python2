#---------------------------------------
# การ loop
# ดึงข้อมูลสมาชิกมาทำงานทีละตัว 

colors4={"red":"สีแดง","yellow":"สีเหลือง","green":"สีเขียว",700:"บ้านแสนสุข"}

for item4 in colors4.keys: # แสดงผลแค่ Keys ** ให้แสดง .value 
    # หากต้องการให้แสดงทั้งคู่เลยให้ .ตามด้วยชื่อตัวแปลที่เราสร้าง เช่น .items
    print(item4)

for k,v in colors4.items():
    print("key = " ,k,"value = ",v)

