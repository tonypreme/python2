# การลบข้อมูล 
# โดยใช้ คำสั่ง pop

colors5={"red":"สีแดง","yellow":"สีเหลือง","green":"สีเขียว",700:"บ้านแสนสุข"}
print(colors5)
colors5.pop("red") # ระบุ key 
print("หลังลบข้อมูล = ",colors5)

# โดยใช้ คำสั่ง popitem 
# เป็นการลบสมาชิกตัวท้ายสุดออกไป
 
colors5.popitem()
print("ลบข้อมูลท้ายสุด = ",colors5)

# โดยใช้ คำสั่ง .clear
# ลบออกทั้งหมดเลย 

colors5.clear()
print("ลบข้อมูลออกไปหมดเลย =",colors5)

