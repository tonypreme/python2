# การสร้าง dict 
'''
โครงสร้าง Dictionary
ชื่อตัวแปร ={key:value,key:value,key:value}
** key:value = 1 ข้อมูล

ถ้าอยากใช้ข้อมูลให้ใช้ key เพื่ออ้างอิง

'''

colors={"red":"สีแดง","yellow":"สีเหลือง","green":"สีเขียว",700:"บ้านแสนสุข"}
print(colors["red"]) # อ้างอิง key รู้ key ได้ value


# การสร้างแบบ constructor เป็นการเปลี่ยนชนิดข้อมูล 
pets=dict(cat="แมว",dog="น้องหมา",duck="น้องเป็ด") 
print(pets["cat"])

