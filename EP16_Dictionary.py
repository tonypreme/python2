# การแก้ไขข้อมูล

colors3={"red":"สีแดง","yellow":"สีเหลือง","green":"สีเขียว",700:"บ้านแสนสุข"}
colors3[700]="บ้านฉัน" # แก้ไขที่ key 700 
print(colors3)

# การเพิ่มข้อมูล  
colors3.update({"blue":"สีน้ำเงิน","orange":"สีส้ม"})
print(colors3)
# ** key เดิมถ้า update จะทำการเปลี่ยน สมาชิก key ** 
# ไม่เจอ key ซ้ำเป็นการเพิ่มเข้าไป ถ้าเจอซ้ำเป็นการแก้ไข 

